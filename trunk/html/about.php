				<h1>About Tesserae</h1>

				<p class="tess">
					Tessera (Latin): 
					1) a small square or block;
					2) a tablet bearing a password; 
					3) a token divided between friends, so they or their descendants can recognize 
						one another when meeting again.
				</p>
				<p>
					Tesserae is a freely available tool for detecting allusions in Latin poetry.
					It was created by the Textual Analysis Working Group, a collaborative, interdisciplinary
					research team based at the University at Buffalo.	
				</p>
				<p>
					Comparison of different texts has been fundamental to the analysis of literary 
					and linguistic meaning since antiquity. It is now possible to envision a computing 
					interface that will allow us to view and navigate through the landscape of 
					similarities between texts.
				</p>
				<p>
					Drawing on the fields of literary studies, linguistics, and computing, we aim to make 
					such a tool freely available online. This site currently offers an early-stage version, 
					along with the most recent results of our ongoing study of the nature of intertextuality.
				</p>
				<p>
					We are currently researching new techniques to measure texts' similarity according to
					semantics, context, sound, and meter.  We are also in the process of expanding our corpus
					to include Greek as well as Latin texts.  This work includes graduate students from Buffalo's
					Department of Classics and Department of Linguistics, as well as collaborative work
					at the VAST Lab of the University of Colorado at Colorado Springs.
				</p>
				<p>
					The Tesserae project has the potential to open up new ways of experiencing the relationships 
					between texts, and the project authors believe that this in turn will lead to truly fresh 
					perspectives and interpretations. In this way, the Tesserae project will contribute to an 
					emerging vision of humanities computing that emphasizes not just the processing of texts, 
					but new, intuitive, and provocative encounters with literature.
				</p>
				<p>
					Our software is open-source and available on request. 
				</p>
